
def gather_values(x):
    '''
    '''
    assert isinstance(x,list)
    
    dic = {}
    for y in x:
        c=0
        assert isinstance(y,str)
        for z in y:
            assert z=='1' or z=='0'
            if z=='1':
                c=c+1
            else:
                c=c-1
        if y not in dic:
            dic[y] = []
            
        if c>=0:
            dic[y].append(1)
        else:
            dic[y].append(0)
        
                
    return dic
    


